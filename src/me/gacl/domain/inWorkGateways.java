package me.gacl.domain;

public class inWorkGateways {
	private String gatewayID;
	private String gatewayManage;
	private String gatewayCreTime;
	private String gatewayState;
	
	public String getGatewayID() {
		return gatewayID;
	}
	public void setGatewayID(String gatewayID) {
		this.gatewayID = gatewayID;
	}
	public String getGatewayManage() {
		return gatewayManage;
	}
	public void setGatewayManage(String gatewayManage) {
		this.gatewayManage = gatewayManage;
	}
	public String getGatewayCreTime() {
		return gatewayCreTime;
	}
	public void setGatewayCreTime(String gatewayCreTime) {
		this.gatewayCreTime = gatewayCreTime;
	}
	public String getGatewayState() {
		return gatewayState;
	}
	public void setGatewayState(String gatewayState) {
		this.gatewayState = gatewayState;
	}
}
